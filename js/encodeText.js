const textInput = document.getElementById("input--text");
const morseOutput = document.getElementById("output--morse");
const encodeButton = document.getElementById("button--encode");
const copyCodeButton = document.getElementById("button--copy-code");

encodeButton.addEventListener("click", () => {
  morseOutput.value = textInput.value
    .toUpperCase()
    .split("")
    .map((char) => morseCode[char] || char)
    .join(" ");
});

copyCodeButton.addEventListener("click", () => {
  morseOutput.select();
  document.execCommand("copy");
});
